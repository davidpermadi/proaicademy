// Generates proai-env.js (the browser's public runtime config) from environment
// variables. Run via `npm run build`. On a hosting platform, set the env vars in the
// dashboard and use `npm run build` as the build command.
//
// Resolution order for each value: process.env  ->  ./.env file  ->  built-in default.
// Only PUBLIC values are emitted here. Secrets (service_role, Midtrans SERVER key) are
// never written to this file.
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');

// Minimal .env parser (no dependency).
function loadDotEnv(file) {
  const out = {};
  try {
    const text = fs.readFileSync(file, 'utf8');
    for (const raw of text.split(/\r?\n/)) {
      const line = raw.trim();
      if (!line || line.startsWith('#')) continue;
      const eq = line.indexOf('=');
      if (eq === -1) continue;
      const key = line.slice(0, eq).trim();
      let val = line.slice(eq + 1).trim();
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
        val = val.slice(1, -1);
      }
      out[key] = val;
    }
  } catch (_) { /* no .env file — fine */ }
  return out;
}

const fileEnv = loadDotEnv(path.join(ROOT, '.env'));
const pick = (k, def) => (process.env[k] != null ? process.env[k] : (fileEnv[k] != null ? fileEnv[k] : def));

