  SUPABASE_PUBLISHABLE_KEY: 'sb_publishable_OMCnXrtMw-Dya2a1Z3g7HA_2GUzR0lb',
  MIDTRANS_CLIENT_KEY: 'Mid-client--uirV-i8Evuc_hW2', // production client key (browser-safe)
  MIDTRANS_IS_PRODUCTION: 'true',
