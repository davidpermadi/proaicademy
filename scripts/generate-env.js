const banner = '/* AUTO-GENERATED by scripts/generate-env.js — do not edit by hand.\n' +
  ' * Public runtime config for the browser. Regenerate with `npm run build`. */\n';
const body = 'window.PROAI_ENV = ' + JSON.stringify(env, null, 2) + ';\n';

